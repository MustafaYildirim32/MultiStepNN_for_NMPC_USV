function dx = cont_derivatives(x, thrust)
    % CONT_DERIVATIVES Compute the derivatives of the state vector.
    %
    %   dx = CONT_DERIVATIVES(x, thrust) returns the derivative vector dx
    %   based on the current state vector x and thrust inputs.
    %
    %   Parameters:
    %       x       - Current state vector (3x1 vector).
    %       thrust  - Thrust inputs (2x1 vector).
    %
    %   Returns:
    %       dx      - Derivative of the state vector (3x1 vector).

    % Initialize the derivative vector
    dx = zeros(size(x));

    % Compute dx(1)
    dx(1) = -0.55245 * x(1) ...
            + (-0.00653252) * (x(3)^2) ...
            + 0.869158 * x(2) * x(3) ...
            + (-0.0833071) * abs(x(1)) * x(1) ...
            + 0.0320901 * sum(thrust);

    % Compute dx(2)
    dx(2) = -0.57293 * x(2) ...
            + (-0.0726335) * x(3) ...
            + (-0.119536) * abs(x(2)) * x(2) ...
            + 0.0896787 * abs(x(3)) * x(3) ...
            + (-0.000300261) * (thrust(1) - thrust(2)) ...
            + (-1.06866) * x(1) * x(3) ...
            + (-0.0177022) * x(1) * x(2);

    % Compute dx(3)
    dx(3) = 0.028676 * x(2) ...
            + (-0.49859) * x(3) ...
            + 0.0112123 * abs(x(2)) * x(2) ...
            + (-0.435785) * abs(x(3)) * x(3) ...
            + 0.0325477 * (thrust(1) - thrust(2)) ...
            + 0.0600072 * x(1) * x(3) ...
            + 0.0077453 * x(1) * x(2);
end
