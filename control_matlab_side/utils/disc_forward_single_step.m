function x_next = disc_forward_single_step(x, thrust)
    % CONT_DERIVATIVES Compute the derivatives of the state vector.
    %
    %   x_next = CONT_DERIVATIVES(x, thrust) returns the derivative vector x_next
    %   based on the current state vector x and thrust inputs.
    %
    %   Parameters:
    %       x       - Current state vector (3x1 vector).
    %       thrust  - Thrust inputs (2x1 vector).
    %
    %   Returns:
    %       x_next      - Derivative of the state vector (3x1 vector).

    % Initialize the derivative vector
    x_next = zeros(size(x));

    % Compute x_next(1)
    x_next(1) = x(1) + (-0.55245 * x(1) ...
            + (-0.00653252) * (x(3)^2) ...
            + 0.869158 * x(2) * x(3) ...
            + (-0.0833071) * abs(x(1)) * x(1) ...
            + 0.0320901 * sum(thrust))*0.1;

    % Compute x_next(2)
    x_next(2) = x(2)+0.1*(-0.57293 * x(2) ...
            + (-0.0726335) * x(3) ...
            + (-0.119536) * abs(x(2)) * x(2) ...
            + 0.0896787 * abs(x(3)) * x(3) ...
            + (-0.000300261) * (thrust(1) - thrust(2)) ...
            + (-1.06866) * x(1) * x(3) ...
            + (-0.0177022) * x(1) * x(2));

    % Compute x_next(3)
    x_next(3) = x(3)+0.1*(0.028676 * x(2) ...
            + (-0.49859) * x(3) ...
            + 0.0112123 * abs(x(2)) * x(2) ...
            + (-0.435785) * abs(x(3)) * x(3) ...
            + 0.0325477 * (thrust(1) - thrust(2)) ...
            + 0.0600072 * x(1) * x(3) ...
            + 0.0077453 * x(1) * x(2));
end